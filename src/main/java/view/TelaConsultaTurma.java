    public void readTurmasConsult(String nome_turma){
        DefaultTableModel consultaTurmas = (DefaultTableModel) tabelaConsultaTurma.getModel();
        consultaTurmas.setNumRows(0);
        TurmaDao turmaDao = new TurmaDao();

        for(Turma turma: turmaDao.readTurmasConsult(nome_turma)){

            consultaTurmas.addRow(new Object[]{
                turma.getId_turma(),
                turma.getNome_turma(),
                turma.getPeriodo(),
                turma.getId_professor()

                

            });
        }      
    }
    
    public void readTelaConsultaTurma(){
        DefaultTableModel consultaTurmas = (DefaultTableModel) tabelaConsultaTurma.getModel();
        consultaTurmas.setNumRows(0);
        TurmaDao turmaDao = new TurmaDao();

        for(Turma turma: turmaDao.read()){

            consultaTurmas.addRow(new Object[]{
                turma.getId_turma(),
                turma.getNome_turma(),
                turma.getPeriodo(),
                turma.getId_professor()

            });
        }


    }
    
