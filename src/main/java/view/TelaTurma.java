    private void jButtonInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInserirActionPerformed
        String nome, periodo;
        
        nome = jTextFieldNomeTurma.getText();
        periodo = jTextFieldPeriodo.getText();
        
        TurmaDTO turmaDto = new TurmaDTO();
        turmaDto.setNomeTurma_cadastro(nome);
        turmaDto.setPeriodo_cadastro(periodo);
        
        TurmaDao turmaDao = new TurmaDao();
        turmaDao.create(turmaDto);
    }//GEN-LAST:event_jButtonInserirActionPerformed

    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed
        String nome_turma, periodo_turma, id_turma;
        
        id_turma = jTextFieldNomeTurma.getText();
        nome_turma = JOptionPane.showInputDialog(null,"Digite o novo nome da turma: ");
        periodo_turma = JOptionPane.showInputDialog(null,"Digite o novo per√≠odo da turma: ");
        
        TurmaDTO turmaDto = new TurmaDTO();
        turmaDto.setNomeTurma_cadastro(nome_turma);
        turmaDto.setPeriodo_cadastro(periodo_turma);
        turmaDto.setId_turma(id_turma);
        
        TurmaDao turmaDao = new TurmaDao();
        turmaDao.update(turmaDto);
    }//GEN-LAST:event_jButtonAlterarActionPerformed

