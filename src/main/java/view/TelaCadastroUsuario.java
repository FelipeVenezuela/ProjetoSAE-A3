    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed
        String nome, cpf, login, senha;
        
        nome = jTextFieldNome.getText();
        cpf = jTextFieldCpf.getText();
        login = jTextFieldLogin.getText();
        senha = jTextFieldSenha.getText();
        
        UserDTO cadastroDto = new UserDTO();
        cadastroDto.setNome_cadastro(nome);
        cadastroDto.setCpf_cadastro(cpf);
        cadastroDto.setLogin_cadastro(login);
        cadastroDto.setSenha_cadastro(senha);
        
        UserDao userDao = new UserDao();
        userDao.cadastrarUser(cadastroDto);
    }//GEN-LAST:event_jButtonCadastrarActionPerformed

